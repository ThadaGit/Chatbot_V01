# ประโยคที่มีอยู่
ประโยค = ["ประโยคแรก", "ประโยคสอง", "ประโยคที่มีคำแรกและสอง", "ประโยคอื่นๆ"]

# คำที่ต้องการลบ
คำล็อค = "แรก"

# สร้างรายการใหม่เพื่อเก็บประโยคที่ไม่มีคำล็อค
รายการใหม่ = [ประโยค for ประโยค in ประโยค if คำล็อค not in ประโยค]

# แสดงประโยคที่ไม่มีคำล็อค
for ประโยค in รายการใหม่:
    print(ประโยค)
